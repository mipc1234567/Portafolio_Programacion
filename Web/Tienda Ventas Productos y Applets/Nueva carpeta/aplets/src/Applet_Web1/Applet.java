   public void calculos(){
        
       
        String producto = null;
        String Venta=null;
        String Ganacia=null;
        //se inicializan las variables dando valores
        producto=jTextField2.getText();
        Venta=jTextField3.getText();
        Ganacia=jTextField4.getText();
        //con un valor de ganancia del 60% sobre el producto
        int ganancia;
        int venta;
        String[]valores= {"aleaciones", "protoboard","leds","cables","compuertas NAND","compuertas OR"
                + "compuertas NOT","bombillos","multimetros","resistencias"};
        for (int i =0; i<=10;i++) {
            
        if(producto==valores[i]){
         venta=Integer.parseInt(Venta);
         ganancia= (venta*60)/100;
         
        }else{
            JOptionPane.showMessageDialog(this, "no se encontro su producto");
        }}
        
      
    }
   
